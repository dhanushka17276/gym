    private void updatePackage(){
        
         PackageDetails packDetails=new PackageDetails();
         
         String name = txtName.getText();
        String price =txtPrice.getText();
        String months = txtMonth.getText();
        
         if(selectedId!=0){
             
             if (name.equals("") || price.equals("") || months.equals("")) {
            JOptionPane.showMessageDialog(null, "Plase fill all details");
            }else{
            
            packDetails.setName(name);
            packDetails.setPrice(Double.parseDouble(price));
            packDetails.setValidTime(Integer.parseInt(months));
            packDetails.setPackageId(selectedId);
            
            boolean isSuccess=packDao.updatePackage(packDetails);
            
            if(isSuccess==true){
                JOptionPane.showMessageDialog(null, "Packege Updated"); 
                DefaultTableModel dm = (DefaultTableModel) jTable1.getModel();
                dm.getDataVector().removeAllElements();
                dm.fireTableDataChanged();
                getAllPackages();
            }else{
                JOptionPane.showMessageDialog(null, "Packege Not Updated . Try Agian!", "Warning", JOptionPane.WARNING_MESSAGE);
            }
             } 
         }
    }

