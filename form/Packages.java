    private void jTable1MouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseReleased
        if(evt.isPopupTrigger()){
            JTable source = (JTable)evt.getSource();
            int row = source.rowAtPoint( evt.getPoint() );
            int column = source.columnAtPoint( evt.getPoint() );

            if (! source.isRowSelected(row))
            source.changeSelection(row, column, false, false);

            jPopupMenu1.show(evt.getComponent(), evt.getX(), evt.getY());
        }
    }//GEN-LAST:event_jTable1MouseReleased

    private void getAllPackages(){
        List<PackageDetails> packList=new ArrayList<>();
        packList=packDao.getAllPackages();
        int id=0;
        int months = 0;
        String name="";
        double price=0;
        
        if(packList.size()>0){
            for(int i=0; i<packList.size();i++){
                
                id=packList.get(i).getPackageId();
                months=packList.get(i).getValidTime();
                name=packList.get(i).getName();
                price=packList.get(i).getPrice();
                String toData[]={String.valueOf(id),name,String.valueOf(price),String.valueOf(months)};
                DefaultTableModel tbMdel=(DefaultTableModel) jTable1.getModel();
                tbMdel.addRow(toData);
            }
        }
        
    }
    
