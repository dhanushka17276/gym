    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        User user=userDao.getLoggedUserDetails();
        
        if(user.getUserType().equals("admin")){
       
        if(selectedId==0){
        
        String name = txtName.getText();
        String price =txtPrice.getText();
        String months = txtMonth.getText();

        PackageDetails packDetails = new PackageDetails();

        if (name.equals("") || price.equals("") || months.equals("")) {
            JOptionPane.showMessageDialog(null, "Plase fill all details");
        } else {
            packDetails.setName(name);
            packDetails.setPrice(Double.parseDouble(price));
            packDetails.setValidTime(Integer.parseInt(months));

            boolean isSucess = packDao.savePackage(packDetails);
            if (isSucess == true) {
                JOptionPane.showMessageDialog(null, "New Packege Added");
                DefaultTableModel dm = (DefaultTableModel) jTable1.getModel();
                dm.getDataVector().removeAllElements();
                dm.fireTableDataChanged();
                getAllPackages();
                txtMonth.setText("");
                txtName.setText("");
                txtPrice.setText("");
            } else {
                JOptionPane.showMessageDialog(null, "Packege Not Added . Try Agian!", "Warning", JOptionPane.WARNING_MESSAGE);
            }
        }

       }else{
            updatePackage();
        }
        
    }else{
            JOptionPane.showMessageDialog(null,"You have not permission to add or change packages" , "Warning",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

