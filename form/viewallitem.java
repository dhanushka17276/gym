    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        //make a sale
         int index = jTable1.getSelectedRow();
        TableModel tbm = jTable1.getModel();
        String itemId = (String) tbm.getValueAt(index, 0);
        User user=userDao.getLoggedUserDetails();
        
        if(user.getUserType().equals("admin")){
            if(stockUserIndex==0){
                 JOptionPane.showMessageDialog(null, "You can't make a sale as admin.Please filter items according to a coach first." , "Warning",JOptionPane.WARNING_MESSAGE);
            }else{
                List<User> userList=userDao.getAllUsersByType("coach");
                User selectedUser=userList.get(stockUserIndex-1);
                  JFrame item=new Singleitem(Integer.parseInt(itemId),selectedUser.getUserId());
                  item.setVisible(true); 
            }
           
        }else{
            
            JFrame item=new Singleitem(Integer.parseInt(itemId),user.getUserId());
             item.setVisible(true); 
        }
       
    }//GEN-LAST:event_jMenuItem1ActionPerformed

    private void jTable1MouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseReleased
       if(evt.isPopupTrigger()){
          JTable source = (JTable)evt.getSource();
            int row = source.rowAtPoint( evt.getPoint() );
            int column = source.columnAtPoint( evt.getPoint() );
            
             if (! source.isRowSelected(row))
                source.changeSelection(row, column, false, false);
            
         jPopupMenu1.show(evt.getComponent(), evt.getX(), evt.getY());
      }
    }//GEN-LAST:event_jTable1MouseReleased

