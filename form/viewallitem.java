    public void getAllItems(){
        
         ArrayList<Item> itemList;
         
         if(userDao.getLoggedUserDetails().getUserType().equals("admin")){
               itemList=(ArrayList<Item>) stockDao.getAllItems();
         }else{
             itemList= (ArrayList<Item>) stockDao.searchItems(0, "",0);
         }
          
    
        String itemId,category,name,price,stock;
        if(itemList.size()>0){
            for(int i=0;i < itemList.size();i++){
                category=itemList.get(i).getCategory();
                name=itemList.get(i).getName();
                itemId=String.valueOf(itemList.get(i).getId());
                price=String.valueOf(itemList.get(i).getPrice());
                stock=String.valueOf(itemList.get(i).getStock());
                String toData[]={itemId,category,name,price,stock};
                DefaultTableModel tbMdel=(DefaultTableModel) jTable1.getModel();
                tbMdel.addRow(toData);
            }
        }
    }

